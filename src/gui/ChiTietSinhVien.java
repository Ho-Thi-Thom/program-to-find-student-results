    private void Thongtinchitiet(String masv, String ten, String phai,
            String noisinh, String ngaysinh, String lop, String nganh,
            String khoahoc, String diemtl10, String diemtl4, String sotinchi,
            String somon, AllPointId alpointid) {
        lbTenSinhVien.setText(ten);
        lbDiemTichLuyHe10.setText(diemtl10);
        lbDiemTichLuyHe4.setText(diemtl4);
        lbNgaySinh.setText(ngaysinh);
        lbKhoaHoc.setText(khoahoc);
        lbLop.setText(lop);
        lbNganh.setText(nganh);
        lbNoiSinh.setText(noisinh);
        lbPhai.setText(phai);
        lbSoMonDaHoc.setText(somon);
        lbSoTinChiTichLuy.setText(sotinchi);
        DefaultTableModel model;
        AllPointId ap = alpointid;
        ArrayList<AllPoint> points = new ArrayList<>();
        points = (ArrayList<AllPoint>) ap.getAllPoint();
        model = (DefaultTableModel) tblDanhSachDiemTungMon.getModel();
        tblDanhSachDiemTungMon.setRowSorter(new TableRowSorter<TableModel>(model));
        model.setColumnIdentifiers(new Object[]{
            "STT", "Mã Môn Học", "Tên Môn Học", "Điểm hệ 10", "Điểm hệ 4", "Xếp loại"});
        int i = 1;
        for (AllPoint point : points) {
            model.addRow(new Object[]{
                i++, point.getMamon(), point.getTenmon(), point.getDiem10(), point.getDiem4(), point.getXeploai()
            });
        }
    }

